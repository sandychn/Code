const int N = 150050;

int a[N];

int main() {
    int T;
    scanf("%d", &T);
    while (T--) {
        int n;
        scanf("%d", &n);
        for (int i = 1; i <= n; ++i) scanf("%d", a + i);
        int Max = INF;
        int ans = 0;
        for (int i = n; i >= 1; --i) {
            if (a[i] > Max) {
                ++ans;
            }
            Max = min(Max, a[i]);
        }
        printf("%d\n", ans);
    }
    return 0;
}